# Listas
n1, n2, n3 = [1, 2, 3]

print(n1)
print(n2)
print(n3, '\n')

# Tuplas
n1, n2 = ('Erick', "Lopes")
print(n1)
print(n2, '\n')

# String
l1, l2 = 'oi'

print(l1)
print(l2, '\n')

# descartar um item
n1, n2, _ = [1, 2, 3]

print(n1)
print(n2, '\n')

# descartar um item
n1, _, n2 = [1, 2, 3]

print(n1)
print(n2)
